--------------------------------------------------------------------------------
function I18N.onSetLanguageOverride ( sCode )
--------------------------------------------------------------------------------
	
	this.sLanguageOverride ( sCode )
    
    application.setCurrentUserEnvironmentVariable ( "language", sCode )
    
	this.cleanUp( )
	
--------------------------------------------------------------------------------
end
